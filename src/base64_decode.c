size_t base64_decode(unsigned char *buffer, const char *string, size_t len, int options)
{
    if (len + 1 == 0)
        len = strlen(string);

    uint8_t *out = buffer;
    const uint8_t *in = (const uint8_t *)string;
    const uint8_t *end = in + len;
    const int8_t *reverse_alphabet = options & Base64UseUrlAlphabet ?
                                         base64url_reverse_alphabet :
                                         base64_reverse_alphabet;
    bool done = false;
    while (!done && in < end) {
        if (reverse_alphabet[in[0]] < 0 || reverse_alphabet[in[1]] < 0) {
            if (in[0] == '\0')
                done = true;
            break;
        }

        uint32_t val = reverse_alphabet[in[0]] << 18;
        val |= reverse_alphabet[in[1]] << 12;
        if (in[2] == '=' || in[2] == '\0') {
            if (in[2] == '=' && (in[3] != '=' || in[4] != '\0'))
                break;
            val >>= 12;
            done = true;
        } else if (in[3] == '=' || in[3] == '\0') {
            if (in[3] == '=' && in[4] != '\0')
                break;
            val >>= 6;
            val |= reverse_alphabet[in[2]];
            done = true;
        } else {
            val |= reverse_alphabet[in[2]] << 6;
            val |= reverse_alphabet[in[3]];
        }

        *out++ = val >> 16;
        *out++ = val >> 8;
        *out++ = val;
        in += 4;
    }

    return out - buffer;
}
